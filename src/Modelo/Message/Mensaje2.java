    public Mensaje2(java.awt.Frame parent, boolean modal) {
        super(parent, modal);
        initComponents();
        //Icon1.setImage(IconFontSwing.buildIcon(GoogleMaterialDesignIcons.CHECK_CIRCLE, 60, new Color(84,193,71), new Color(113, 74, 67)));
        
        setOpacity(0f);
        this.setLocationRelativeTo(null);
        getContentPane().setBackground(Color.WHITE);
        TimingTarget target = new TimingTargetAdapter() {
            @Override
            public void timingEvent(float fraction) {
                if (show) {
                    setOpacity(fraction);
                } else {
                    setOpacity(1f - fraction);
                }
            }

            @Override
            public void end() {
                if (show == false) {
                    setVisible(false);
                }
            }

        };
        animator = new Animator(200, target);
        animator.setResolution(0);
        animator.setAcceleration(0.5f);
    }

    public void showMessage(String message) {
        Mensaje1.setText(message);
        animator.start();
        setVisible(true);
    }
                                     
